package org.example;

public class Calculator {

    public double add(double x, double y) {
        return x + y;
    }

    public double sub(double x, double y) {
        return x - y;
    }

    public double multi(double x, double y) {
        return x * y;
    }

    public double division (double x, double y) {
        return x / y;

    }
}
